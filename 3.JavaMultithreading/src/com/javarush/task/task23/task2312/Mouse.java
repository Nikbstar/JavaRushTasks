package com.javarush.task.task23.task2312;

public class Mouse {

    private int x;
    private int y;

    public Mouse(final int x, final int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return this.x;
    }

    public int getY() {
        return this.y;
    }
}
